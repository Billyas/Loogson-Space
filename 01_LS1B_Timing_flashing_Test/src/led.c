 #include "led.h"

 /*************************************************************************************
 **函数功能：初始化led的IO口
 **说明：
 **************************************************************************************/
 void LED_init(void)
 {
    gpio_enable(LED_1,DIR_OUT);

    LED_ALL_OFF();
 }
/*************************************************************************************
 **函数功能：LED灯亮
 **说明：
 **************************************************************************************/
 void LED_ALL_ON(void)
 {
    gpio_write(LED_1,LED_ON);
 }
/*************************************************************************************
 **函数功能：LED灯灭
 **说明：
 **************************************************************************************/
 void LED_ALL_OFF(void)
 {
    gpio_write(LED_1,LED_OFF);
 }



